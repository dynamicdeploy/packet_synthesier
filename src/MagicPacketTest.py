import unittest
from MagicPacket import MagicPacket

class MagicPacketTest(unittest.TestCase):

    def testSimpleGenerate(self):
        context = {}
        mp = MagicPacket(context)
        self.assertIsNotNone(mp.generatePacket())

    def testGenerateMagicPacket(self):
        context = {}
        mp = MagicPacket(context);
        expected = '\xff\xff\xff\xff\xff\xff\x01\x02\x03\x04\x05\x06\x08B\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff'
        self.assertEquals(expected, mp.generatePacket())


if __name__ == "__main__":
    #import sys;sys.argv = ['', 'Test.testName']
    unittest.main()